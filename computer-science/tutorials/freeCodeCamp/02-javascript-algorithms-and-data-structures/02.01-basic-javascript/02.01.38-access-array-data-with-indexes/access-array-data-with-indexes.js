/**
 * You can access the data inside arrays using `indexes`.
 *
 * Array indexes are written in the same bracket notation that
 * strings use, except that instead of specifying a character,
 * they are specifying an entry in the array. Like strings, arrays
 * use *zero-based* indexing, so the first element in an array is element `0`.
 */

var ourArray = [50, 60, 70];
var ourData = ourArray[0]; // equals 50

// Setup:
var myArray = [50, 60, 70];
// Create a variable called `myData` and set it to equal the first value of `myArray` using bracket notation:
var myData = myArray[0]; // equals 50
